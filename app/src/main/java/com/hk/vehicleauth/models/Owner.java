package com.hk.vehicleauth.models;

public class Owner {
    private String name, nid, email, pass;

    public Owner() {
    }

    public Owner(String name, String nid, String email, String pass) {
        this.name = name;
        this.nid = nid;
        this.email = email;
        this.pass = pass;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNid() {
        return nid;
    }

    public void setNid(String nid) {
        this.nid = nid;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }
}
